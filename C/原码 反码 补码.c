#include <stdio.h>
int main()
{
	float a=6.9f;
	printf("%d\n",(int)a);
	
	printf("%x\n",-54);
	printf("%x\n",54);
	/*
	4个字节，1个字节=8bit
	-54
	原码 1000 0000 0000 0000 0000 0000 0011 0110
	反码 1111 1111 1111 1111 1111 1111 1100 1001
	补码 1111 1111 1111 1111 1111 1111 1100 1010
   16制  ff ff ff ca
   
   54
	原码 反码 补码
	 0000 0000 0000 0000 0000 0000 0011 0110
   16制  00 00 00 36
   
   
	
	正数 原码 反码 补码 相同
	负数 
	反码 将原码中除符号位以外的取反
	补码 反码+1就是补码
	*/
	return 0;
}